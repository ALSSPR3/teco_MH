package basic.ch01;

public class DataType4 {

	// 코드의 시작점
	public static void main(String[] args) {
		
		boolean isMarryed = false;
		System.out.println(isMarryed);
		isMarryed = true;
		System.out.println(isMarryed);
		
		boolean isOk;
		isOk = true;
		System.out.println(isOk);
		
		System.out.println("------------------");
		System.out.println(true);
		System.out.println(false);
		
		
	} // end of main
} // end of class
